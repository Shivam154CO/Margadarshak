export const exportDetailedCollegeReport = (
  college: College,
  insights: string,
  profile: any,
  filename: string = 'detailed-college-report.pdf'
) => {
  if (!college) return;
  const doc = new jsPDF();
  const pageWidth = doc.internal.pageSize.getWidth();
  const pageHeight = doc.internal.pageSize.getHeight();
  const margin = 14;

  /** PAGE 1: STRATEGIC SUMMARY & BRANDING **/
  // Branded Header
  doc.setFillColor(15, 23, 42); // Slate 900
  doc.rect(0, 0, pageWidth, 60, 'F');
  
  doc.setTextColor(255, 255, 255);
  doc.setFont("helvetica", "bold");
  doc.setFontSize(22);
  doc.text(college.college_name.toUpperCase(), margin, 25, { maxWidth: pageWidth - 40 });
  
  doc.setFontSize(10);
  doc.setFont("helvetica", "normal");
  doc.text(`${college.city}, MS | Estd. ${college.established_year || '1960'} | ${college.accreditation || 'NAAC Accredited'}`, margin, 45);
  doc.text(`Strategic Admission Profile for Rank: ${profile?.cet_rank || profile?.diploma_rank}`, margin, 52);

  // Strategic Verdict Area
  doc.setTextColor(51, 65, 85); 
  doc.setFontSize(18);
  doc.setFont("helvetica", "bold");
  doc.text("1. STRATEGIC POSITIONING", margin, 80);
  doc.setDrawColor(99, 102, 241);
  doc.line(margin, 83, margin + 40, 83);

  const verdictLine = insights.split('\n').find(l => l.includes('Verdict:')) || "Verdict: Best Fit";
  const probabilityLine = insights.split('\n').find(l => l.includes('Probability')) || "85% Probability";

  doc.setFontSize(12);
  doc.setTextColor(79, 70, 229); // Indigo
  doc.text(verdictLine.replace(/\*\*/g, ''), margin, 95);
  doc.setFontSize(10);
  doc.setTextColor(100, 100, 100);
  doc.text(probabilityLine.replace(/\*\*/g, ''), margin, 102);

  // Performance Table
  autoTable(doc, {
    startY: 110,
    head: [['Key Performance Metric', 'Data Point']],
    body: [
      ['Placement Rate', `${college.placement_rate || 0}%`],
      ['Average Package', `INR ${college.average_package_lpa || 0} LPA`],
      ['Highest Package', `INR ${college.highest_package_lpa || 0} LPA`],
      ['Campus Reputation', college.accreditation || 'Excellent'],
      ['Student ROI', (college.placement_rate || 0) > 80 ? 'HIGH' : 'MODERATE']
    ],
    theme: 'striped',
    headStyles: { fillColor: [15, 23, 42] }
  });

  doc.setFontSize(9);
  doc.setTextColor(150, 150, 150);
  doc.text("- Page 1: Strategic Admission Overview -", pageWidth / 2, pageHeight - 10, { align: 'center' });

  /** PAGE 2: ADMISSION & ACADEMIC INTELLIGENCE **/
  doc.addPage();
  doc.setFillColor(248, 250, 252);
  doc.rect(0, 0, pageWidth, 40, 'F');
  doc.setTextColor(15, 23, 42);
  doc.setFontSize(16);
  doc.text("ADMISSION & ACADEMIC INTELLIGENCE", margin, 25);

  doc.setFontSize(14);
  doc.text("2. CUTOFF & SEAT MATRIX ANALYSIS", margin, 55);
  doc.line(margin, 57, margin + 30, 57);

  autoTable(doc, {
    startY: 65,
    head: [['Academic Stream', 'Matrix Category', 'Intake', 'Prev Rank']],
    body: [
      [college.branch_name || 'Stream A', 'GOPEN', college.seats || 60, college.cutoff_rank || 'N/A'],
      [college.branch_name || 'Stream A', 'LOPEN', 'N/A', 'N/A'],
      [college.branch_name || 'Stream A', 'DEF/PH', 'N/A', 'N/A']
    ],
    theme: 'grid'
  });

  doc.setFontSize(14);
  doc.text("3. FEE EXPOSITION", margin, (doc as any).lastAutoTable.finalY + 15);
  
  autoTable(doc, {
    startY: (doc as any).lastAutoTable.finalY + 22,
    head: [['Category Breakdown', 'Annual Fees (INR)']],
    body: [
      ['OPEN / General', `INR ${college.fees || '0'}`],
      ['OBC / EBC (50%)', `INR ${(college.fees || 0) / 2}`],
      ['SC / ST (Free-ship)', 'INR 0 (Excluded Exams)'],
      ['TFWS / EWS', `INR ${(college.fees || 0) * 0.1}`]
    ],
    theme: 'striped'
  });

  doc.setFontSize(14);
  doc.text("4. MANDATORY DOCUMENT CHECKLIST", margin, (doc as any).lastAutoTable.finalY + 15);
  const docs = ["SSC/HSC Marksheets", "CET/JEE Scorecard", "LC/TC Transfer", "Domicile Certificate", "Income Certificate"];
  doc.setFontSize(10);
  docs.forEach((d, i) => {
    doc.text(`[ ] ${d}`, margin + 5, (doc as any).lastAutoTable.finalY + 25 + (i * 7));
  });

  doc.text("- Page 2: Academic & Financial Analysis -", pageWidth / 2, pageHeight - 10, { align: 'center' });

  /** PAGE 3: DEEP-DIVE AI INSIGHTS & CAREER **/
  doc.addPage();
  doc.setFillColor(15, 23, 42);
  doc.rect(0, 0, pageWidth, 30, 'F');
  doc.setTextColor(255, 255, 255);
  doc.setFontSize(14);
  doc.text("DEEP-DIVE AI ANALYSIS & CAREER OUTLOOK", margin, 20);

  doc.setTextColor(15, 23, 42);
  doc.setFontSize(14);
  doc.text("5. PROFESSIONAL AI BRIEFING", margin, 50);
  doc.line(margin, 52, margin + 35, 52);

  doc.setFontSize(10);
  const cleanInsights = insights
    .replace(/### /g, '')
    .replace(/\*\*/g, '')
    .split('\n')
    .filter(line => line.trim() !== '' && !line.includes('Verdict'));

  const splitInsights = doc.splitTextToSize(cleanInsights.join('\n\n'), pageWidth - 28);
  doc.text(splitInsights, margin, 65);

  const insightsY = 65 + (splitInsights.length * 5.5);

  doc.setFontSize(14);
  doc.text("6. TOP RECRUITING PARTNERS", margin, insightsY + 15);
  const recruiters = college.top_recruiters?.split(',') || ['TCS', 'Infosys', 'Wipro', 'Accenture', 'Cognizant', 'Capgemini'];
  
  autoTable(doc, {
    startY: insightsY + 22,
    body: [
        recruiters.slice(0, 3),
        recruiters.slice(3, 6)
    ],
    theme: 'plain',
    styles: { cellPadding: 5, fontSize: 10, fontStyle: 'bold' }
  });

  // Footer Disclaimer
  doc.setFontSize(8);
  doc.setTextColor(150, 150, 150);
  doc.text("This report is generated using Ikigai AI Scraping and Simulation Engine.", margin, pageHeight - 25);
  doc.text("Historical data trends are used to simulate academic and placement environment.", margin, pageHeight - 20);

  doc.setFontSize(9);
  doc.text("- Page 3: Professional Insights & Advisory -", pageWidth / 2, pageHeight - 10, { align: 'center' });

  doc.save(filename || `${college.college_code}-Detailed-Report.pdf`);
};

