export const exportToCSV = (colleges: College[], filename: string = 'ikigai-predictions.csv') => {
  if (!colleges.length) return;

  const headers = ['College Name', 'Branch', 'City', 'Cutoff Rank', 'Admission Chance', 'Fit Category', 'Fees'];
  const rows = colleges.map(c => [
    `"${c.college_name}"`,
    `"${c.branch}"`,
    `"${c.city}"`,
    c.cutoff_rank || 0,
    c.admission_chance || c.admission_chance_percentage || 'N/A',
    c.probability_level || c.fit || 'N/A',
    c.fees || 0
  ]);

  const csvContent = [headers.join(','), ...rows.map(r => r.join(','))].join('\n');
  const blob = new Blob([csvContent], { type: 'text/csv;charset=utf-8;' });
  const url = URL.createObjectURL(blob);
  const link = document.createElement('a');
  link.setAttribute('href', url);
  link.setAttribute('download', filename);
  link.style.visibility = 'hidden';
  document.body.appendChild(link);
  link.click();
  document.body.removeChild(link);
};

export const exportToPDF = (
  colleges: College[], 
  profile: UserProfile | null, 
  aiInsights: string = '',
  filename: string = 'ikigai-report.pdf'
) => {
  if (!colleges.length) return;

  const doc = new jsPDF();
  const pageWidth = doc.internal.pageSize.getWidth();
  const margin = 14;

  // Header Background
  doc.setFillColor(79, 70, 229); // Indigo 600
  doc.rect(0, 0, pageWidth, 45, 'F');

  // Title
  doc.setTextColor(255, 255, 255);
  doc.setFont("helvetica", "bold");
  doc.setFontSize(24);
  doc.text("IKIGAI ADMISSION REPORT", margin, 25);
  
  doc.setFontSize(10);
  doc.setFont("helvetica", "normal");
  doc.text("Generated by Ikigai AI Analysis Engine", margin, 35);

  // User details section
  doc.setTextColor(50, 50, 50);
  doc.setFontSize(12);
  doc.setFont("helvetica", "bold");
  doc.text("CANDIDATE PROFILE", margin, 60);
  
  doc.setLineWidth(0.5);
  doc.setDrawColor(200, 200, 200);
  doc.line(margin, 62, pageWidth - margin, 62);

  doc.setFont("helvetica", "normal");
  doc.setFontSize(10);
  const profileY = 70;
  doc.text(`Name: ${profile?.name || 'Student'}`, margin, profileY);
  doc.text(`Exam: ${profile?.exam_type || 'CET'}`, margin, profileY + 7);
  doc.text(`Rank: ${profile?.exam_type === 'CET' ? profile?.cet_rank : profile?.diploma_rank || 'N/A'}`, pageWidth / 2, profileY);
  doc.text(`Score: ${profile?.exam_type === 'CET' ? profile?.cet_score : profile?.diploma_score || 'N/A'}%`, pageWidth / 2, profileY + 7);

  // AI Insights Section
  let currentY = 95;
  if (aiInsights) {
    doc.setFillColor(243, 244, 246); // Gray 100
    doc.roundedRect(margin, currentY, pageWidth - (margin * 2), 30, 3, 3, 'F');
    
    doc.setTextColor(79, 70, 229); // Indigo 600
    doc.setFont("helvetica", "bold");
    doc.text("AI STRATEGIC INSIGHTS", margin + 5, currentY + 10);
    
    doc.setTextColor(70, 70, 70);
    doc.setFont("helvetica", "normal");
    doc.setFontSize(9);
    const splitText = doc.splitTextToSize(aiInsights, pageWidth - (margin * 2) - 10);
    doc.text(splitText, margin + 5, currentY + 18);
    currentY += 45;
  }

  // Table
  doc.setTextColor(50, 50, 50);
  doc.setFont("helvetica", "bold");
  doc.setFontSize(12);
  doc.text("PREDICTION ANALYSIS", margin, currentY);
  currentY += 5;

  const tableData = colleges.slice(0, 50).map((c, i) => [
    (i + 1).toString(),
    c.college_name || 'N/A',
    c.branch || 'N/A',
    c.city || 'N/A',
    c.cutoff_rank ? c.cutoff_rank.toString() : 'N/A',
    c.probability_level || c.fit || 'N/A',
    `${c.admission_chance || c.admission_chance_percentage || 'N/A'}%`
  ]);

  autoTable(doc, {
    startY: currentY,
    head: [['#', 'College Name', 'Branch', 'City', 'Rank', 'Fit', 'Chance']],
    body: tableData,
    theme: 'striped',
    headStyles: { fillColor: [79, 70, 229], textColor: 255 },
    styles: { fontSize: 8, cellPadding: 2 },
    columnStyles: {
      0: { cellWidth: 8 },
      1: { cellWidth: 60 },
      2: { cellWidth: 40 },
      3: { cellWidth: 25 },
      4: { cellWidth: 15 },
      5: { cellWidth: 22 },
      6: { cellWidth: 15 }
    }
  });

  // Footer on each page
  const pageCount = (doc as any).internal.getNumberOfPages();
  for (let i = 1; i <= pageCount; i++) {
    doc.setPage(i);
    doc.setFontSize(8);
    doc.setTextColor(150, 150, 150);
    doc.text(
      `Page ${i} of ${pageCount} | Generated by Ikigai Smart Analysis | ${new Date().toLocaleDateString()}`,
      pageWidth / 2,
      doc.internal.pageSize.getHeight() - 10,
      { align: 'center' }
    );
  }

  doc.save(filename);
};

